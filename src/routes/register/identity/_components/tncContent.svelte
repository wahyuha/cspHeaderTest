  <p class="ff-b">A. Definisi</p>
  <ol>
    <li>
      <span class="ff-b">Akun LinkAja</span> adalah akun milik Pengguna LinkAja yang
      terdaftar secara resmi dan bisa digunakan untuk berbagai layanan dan fitur
      yang terdapat di dalam Aplikasi LinkAja.
    </li>
    <li>
      <span class="ff-b">Aplikasi LinkAja</span> atau
      <span class="ff-b">LinkAja</span> adalah sebuah aplikasi yang menyediakan layanan
      sistem pembayaran dan transaksi perdagangan melalui sistem elektronik yang
      diselenggarakan oleh Finarya serta telah terdaftar dan diawasi oleh Bank Indonesia
      dan Kementerian Komunikasi dan Informatika Republik Indonesia.
    </li>
    <li>
      <span class="ff-b">Hari Kalender</span> adalah setiap hari mulai dari Senin
      sampai dengan Minggu, termasuk hari libur nasional atau hari libur lain yang
      ditetapkan oleh pemerintah Republik Indonesia.
    </li>
    <li>
      <span class="ff-b">LinkAja Syariah</span> adalah fitur layanan uang elektronik
      yang dikelola sesuai kaidah-kaidah syariat sebagaimana difatwakan dalam Fatwa
      DSN MUI No. 116/DSN-MUI/IX/2017, dan merupakan bagian layanan uang elektronik
      yang diselenggarakan oleh Finarya yang telah terdaftar dan diawasi oleh Bank
      Indonesia. Layanan ini dirancang dengan menggunakan skema Syariah melalui akad
      Qard antara Finarya dan Anda.
    </li>
    <li>
      <span class="ff-b">Pengguna LinkAja</span> adalah Anda yang terdaftar dan memiliki
      Akun LinkAja.
    </li>
    <li>
      <span class="ff-b">Saldo LinkAja</span> adalah saldo uang elektronik yang tersimpan
      dalam Akun milik Pengguna dan digunakan oleh Pengguna dalam melakukan transaksi
      di dalam Aplikasi LinkAja.
    </li>
    <li>
      <span class="ff-b">Saldo Bonus</span> adalah saldo atau cashback yang Anda
      dapatkan dari transaksi promo yang dilakukan oleh Anda melalui Aplikasi LinkAja.
    </li>
  </ol>

  <p class="ff-b">B. Layanan LinkAja</p>
  <ol>
    <li>
      Uang elektronik LinkAja yang diterbitkan oleh Finarya melalui Aplikasi
      LinkAja memiliki fungsi yang sama dengan uang tunai sebagai alat
      pembayaran yang sah, dimana nilainya setara dengan nilai uang tunai yang
      disetorkan terlebih dahulu ke rekening bank konvesional BUKU 4 untuk
      Pengguna Aplikasi LinkAja, dan rekening bank syariah yang terafiliasi
      dengan bank BUKU 4 untuk Pengguna Layanan Syariah LinkAja. Nilai uang yang
      disetorkan tersebut bukanlah bersifat simpanan sebagaimana diatur dalam
      peraturan perundang-undangan perbankan dan oleh karenanya LinkAja tidak
      memberikan bunga serta tidak dijamin oleh Lembaga Penjamin Simpanan.
    </li>
    <li>
      LinkAja lahir pada tahun 2019 untuk menjadi wadah sinergi bagi semua
      layanan uang elektronik yang sebelumnya diterbitkan oleh Badan Usaha Milik
      Negara (BUMN), seperti PT Telekomunikasi Selular dengan merek TCASH; PT
      Bank Mandiri (Persero) Tbk. dengan merek E-Cash; PT Bank Negara Indonesia
      (Persero) Tbk. dengan merek UnikQu; dan PT Bank Rakyat Indonesia (Persero)
      Tbk. dengan merek T-Bank.
    </li>
    <li>
      Pemegang layanan uang elektronik TCASH, E-Cash, UnikQu dan/atau T-Bank,
      yang sampai dengan tanggal 30 Juni 2019 tidak secara nyata menyatakan
      ketidaksetujuannya untuk melakukan migrasi ke layanan uang elektronik
      LinkAja, dianggap telah setuju untuk melakukan migrasi dari layanan uang
      elektronik tersebut ke layanan uang elektronik LinkAja.
    </li>
    <li>
      Pemegang layanan uang elektronik LinkAja yang berasal dari prosedur
      migrasi akan otomatis menjadi Pengguna LinkAja serta saldo pada layanan
      uang elektronik sebelumnya akan dikonversikan menjadi Saldo LinkAja.
    </li>
    <li>
      Nomor rekening LinkAja adalah nomor telepon selular dari Pengguna LinkAja
      yang tercatat pada Finarya, dimana 1 (satu) Akun LinkAja hanya berlaku
      untuk 1 (satu) nomor telepon selular dalam jaringan Indonesia, dan juga
      sebaliknya.
